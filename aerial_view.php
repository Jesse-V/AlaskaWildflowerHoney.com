                    <p>This is an overview of the area where we live and have our hives.  We are 25 miles straight north of Anchorage on the map, but as you can see a portion of Cook Inlet is between our house and Anchorage.  We are about 60 miles by road and access our place on the Parks Highway (3).  Big Lake is the large ice covered lake that is just north of our location.  The portion of the photo that has our area was taken in early May before the leaves were out in full leaf.  In the picture below Big Lake appears on the top of the picture.  To give some scale of our area I have placed a one mile marker to give an idea of the area that the bees forage.  About a mile and a half is the accepted range of a beeâ€™s forage area.  As the picture indicates there are no agricultural activities and very few houses.  This ensures that the bees are foraging primarily on the natural vegetation of the area.  The viewer may note that there are a couple of areas that look like they may be farm fields.  These are the old abandoned prove up fields that were cleared in 1960 when my family and our neighbors homesteaded the area.  During the wildfire of 1996 the old piled up rows of logs and brush were worked over with heavy equipment to put out the smoldering fires inside them.  The photos that are shown are from Google Earth, a great program that allows you to zoom in on almost any spot on earth.</p>
