                    <h3>March 16, 2014 update</h3>

                    <div class="right_image">
                        <img src="images/closeup_packages.jpg" alt="Crated four-pound package of honeybees."/>
                        <p>These packages weigh 4 pounds and contain about 15,000 bees. The condition of the packages shown here is typical of our shipments over the past several years.</p>
                    </div>

                    <p>Bee orders are filling up.</p>

                    <p><b>We have approximately 150 packages left.</b> At this point we are filling the last shipment day on the 26th of April.</p>

                    <p>California has received a bit of rain and is due to get more this weekend.  That should greatly improve the situation for the bees.</p>

                    <p>Our shipping dates have changed somewhat from last year's method.  A portion of our normal route involved ground transportation by truck.  That portion of our shipment has changed as the trucking company informed us that they will no longer be transporting any live cargo.  Even though our routing and booked space was confirmed before the policy change, our shipments still fall into the live cargo category.  This has caused us to change ground transportation routes and reorganize our delivery schedule as well as the number of packages on each load of bees to arrive.  We have reduced our shipments to three separate shipments.  All shipments will arrive on a Saturday.  This means that we will have a number of the bees for the Anchorage and Eagle River area arrive on the first shipment the 12th of April along with the bees for the Interior.  Most of the bees for Palmer and Wasilla will arrive on the following Saturday along with bees for Soldotna and Big Lake on the 19th.  Those who want their bees later in the month and also those who order bees after allotted space fills up on the two earlier shipments will have their bees arrive on the last Saturday, April 26th.</p>

                    <p>I will be accompanying the bees on the land portion of their journey from the bee yard to the airfreight terminal for at least the first shipment and quite possibly the following shipments.  We still intend to drop off supplies with the bees for those who have ordered supplies from us to be delivered.  However, because of the change of shipping and our focus on the safe arrival of the bees, we will be closing our supply storehouse starting on the first of April.  We will remain closed until the bees have all been safely delivered to their drop points.</p>

                    <p>
                        Planned delivery dates are as follows:<br>
                    &nbsp;&nbsp;&nbsp;Fairbanks and Interior Alaska: April 12th.<br>
                    &nbsp;&nbsp;&nbsp;Anchorage and Eagle River: April 12th.<br>
                    &nbsp;&nbsp;&nbsp;Soldotna and Palmer: April 19th.<br>
                    &nbsp;&nbsp;&nbsp;Wasilla and Big Lake: April 19th.<br>
                    &nbsp;&nbsp;&nbsp;Late orders (all locations): April 26th.
                    </p>

